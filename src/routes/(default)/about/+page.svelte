<h1>About</h1>
<p>
  We're a sing-along band in Tucson, AZ singing fun-loving uplifting songs and
  chants. Find us at*:
</p>
<ul>
  <li>
    Riverfront Park Thursday mornings at 9:00am. First half hour is ecstatic
    dance, followed by an hour or so of singing and chanting until we get
    hungry.
  </li>
  <li>
    Northwest Global Chant at Oro Valley United Church of Christ, 1st, 3rd,
    and 5th Mondays at 6:30pm.
  </li>
  <li>
    Sunset Sundays at James Kreigh Park, at quarter-till-sunset we dance with
    the sunset and end with meditative music while lying on yoga mats
    watching the stars emerge. We call it a mini camping ecstatic dance trip.
  </li>
  <li>
    Usually jamming with other musicians and friends after Spirit
    of Movement Tucson's Ecstatic Dance at Himmel Park, Sunday mornings at
    10:30am.
  </li>
</ul>
<p>
  * Check back for time changes with the seasons or postponed dates.
</p>
